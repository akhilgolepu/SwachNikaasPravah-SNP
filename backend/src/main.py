async def background_telemetry_loop():
    """Simulates persistent frame capture ingestion sequence over open cameras."""
    while True:
        await asyncio.sleep(4.0)  # Stream frequency updates every 4 seconds to dashboard
        if not manager.active_connections:
            continue

        # Select a random asset to organically simulate dynamic threat fluctuations
        target_id = random.choice(list(DRAIN_ASSETS.keys()))
        asset = DRAIN_ASSETS[target_id]
        
        # Keep values normal if not escalated via storm simulation overrides
        if current_states[target_id]["status"] != "CRITICAL":
            current_states[target_id]["blockage"] = min(100, max(0, current_states[target_id]["blockage"] + random.randint(-5, 5)))
            current_states[target_id]["rain_6h"] = min(100, max(0, current_states[target_id]["rain_6h"] + random.randint(-2, 2)))
        
        # Execute the Multi-Signal mathematical score evaluation block
        ri = calculate_risk_fusion_index(
            current_states[target_id]["blockage"],
            current_states[target_id]["rain_6h"],
            asset["topo"]
        )
        current_states[target_id]["risk_score"] = ri

        # Evaluate risk category tiers
        if ri >= 70.0 and current_states[target_id]["status"] != "DISPATCHED":
            current_states[target_id]["status"] = "CRITICAL"
        elif ri >= 45.0 and current_states[target_id]["status"] != "DISPATCHED":
            current_states[target_id]["status"] = "WARNING"
        elif current_states[target_id]["status"] != "DISPATCHED":
            current_states[target_id]["status"] = "NORMAL"

        # Construct payload message packet mapping back to schema specifications
        payload = {
            "event": "TELEMETRY_UPDATE",
            "data": {
                "drain_id": target_id,
                "blockage_ratio": current_states[target_id]["blockage"],
                "incoming_rain_mm": current_states[target_id]["rain_6h"],
                "risk_score": current_states[target_id]["risk_score"],
                "status": current_states[target_id]["status"],
                "timestamp": datetime.utcnow().isoformat() + "Z"
            }
        }
        await manager.broadcast_payload(payload)

