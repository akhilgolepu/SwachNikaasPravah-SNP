def calculate_risk_fusion_index(ab: float, rf: float, vt: float) -> float:
    """
    Computes the Multi-Signal Threat Priority Matrix Score.
    Equation: RI = (0.5 * Ab) + (0.3 * Rf) + (0.2 * Vt)
    Normalized inputs mapping:
      - ab: Blockage Ratio (Percentage 0 - 100)
      - rf: Rainfall prediction forecast mm (Mapped up to a 100mm upper bound threshold)
      - vt: Topological asset vulnerability multiplier (Scale 0.0 - 1.0)
    """
    normalized_rain = min(rf, 100.0)  # Caps max rain factor at 100mm depth normalization
    risk_score = (0.5 * ab) + (0.3 * normalized_rain) + (0.2 * (vt * 100))
    return round(risk_score, 1)

